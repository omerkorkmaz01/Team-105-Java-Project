package ders15_Overloading_whileLoop;

public class C03_WhileLoop {
    public static void main(String[] args) {

        // 2 basamakli sayilari aralarinda bir bosluk olacak seklinde yazdirin

        for (int i = 10; i <=99 ; i++) {
            System.out.print(i+" ");
        }
        System.out.println(" ");
        // while loop ile yaparsak

        int a=10;
        while (a<100){
            System.out.print(a+" ");
            a++;
        }

    }
}
